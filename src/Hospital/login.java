    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed

        String name = this.name.getText();
        String type = this.jComboBoxGender.getSelectedItem().toString();
        String pass = String.valueOf(this.pass.getPassword());

        if ((name.equals(""))) {
            JOptionPane.showMessageDialog(null, "Enter your Full Name");
        } else if ((pass.equals(""))) {
            JOptionPane.showMessageDialog(null, "Enter your Password");
        } else {
            Stack<String[]> stack = getStack();
            boolean login = false;
            int size=stack.size();
            for (int i = 0; i < size; i++) {    
               
                String strr[] = stack.pop();
                if (strr[0].equals(name) && strr[1].equals(type) && strr[2].equals(pass)) {
                    login = true;
                }
            }

            if (login) {
                JOptionPane.showMessageDialog(null, "Login Succesful!");
                LoginPersonName = name;
                LoginPersonType = type;
                new DocPanel().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Login Failed!");
            }
        }

    }//GEN-LAST:event_submitActionPerformed

