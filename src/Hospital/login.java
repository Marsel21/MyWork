    public Stack<String[]> getStack() {
       Stack<String[]> stack = new Stack<String[]>();
        try {
            BufferedReader bfReader = new BufferedReader(new FileReader("src/login.txt"));
            String str = bfReader.readLine();
            while (str != null) {
                stack.push(str.split(", "));
                str = bfReader.readLine();
            }
            bfReader.close();
        } catch (Exception ex) {
            System.out.println(ex);
        }
        return stack;
    }

